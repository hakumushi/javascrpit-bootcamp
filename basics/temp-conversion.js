let fahrenheit = 110

// calculate celcius and store in celsius variable
// print the value
let celsius = (fahrenheit - 32) / 1.8
console.log('Celsius: ' + celsius.toFixed(2))

// calculate kelvin and store in kelvin variable
// print the value
let kelvin = celsius + 273.15
console.log('Kelvin: ' + kelvin.toFixed(2))
